 
<h3>Februar 2010: Neuauflage der IHK-Geschichte?</h3>
In Deutschland gibt es keinen Standard für Ausbildungen auf dem Gebiet des Ayurveda. Im Gegensatz zu Österreich wo für die Berufsbezeichnung "Ayurveda-Praktiker" ausführliche Vorgaben zu Art und Umfang der  Ausbildung existieren.
<p>
Vor einiger Zeit gab es Bestrebungen mithilfe der Industrie und Handelskammer Ayurveda wie auch Yoga zu einem IHK-Ausbildungsberuf zu machen.
Es gab Firmen, die versuchten mithilfe der IHK ein Geschäft daraus zu machen.
Die Auseinandersetzungen zu diesem Thema füllen Bände, Sie können einiges davon auf den Seiten des UBAI finden.
<br>Zum Glück ist das Thema IHK-Yoga und IHK-Ayurveda in den Annalen dubioser Geschäftemacherei verschwunden.
<p>Der Grund ist eigentlich offensichtlich: Die Industrie und Handelskammer ist einfach nicht zuständig. Bei Ayurveda geht es um Gesundheit oder das, was man heute als Wellness bezeichnet, jedenfalls aber nicht um Industrie und nicht um Handel.
<h5>Was ist denn Ayurveda-Fachberater (IHK)?</h5>
Auf einmal fallen wieder Anzeigen auf, die jetzt doch einen IHK-Ayurveda-Beruf bewerben. Genannt wirds nun <i>Ayurveda-Fachberater (IHK)</i>.
<p>Was ist das?
<br>Offensichtlich hat die rege IHK Saarland einen Weg gefunden das schöne Wort Ayurveda doch noch in einer Berufsbeschreibung zu verwenden, 
die  mit Industrie oder Handel zu tun hat.
Dabei geht es nun nicht mehr um Gesundheit oder Gesunderhaltung, sondern um "unternehmerisches Handeln, Management und auf den Markt abgestimmte Dienstleistungs und Beratungsangebote". Auch für "kaufmännisch ausgerichtete Interessenten ohne Ayurveda-Background".

