
<h3>IHK und Yoga</h3>
Im UBAI sind natürlich auch YOGA-Lehrer/Innen organisiert.
<br>Was bisher geschah - auf dem Gebiet des Ayurvdas hat ein ähnliches Vorspiel im Jahr 2006 auf dem Gebiet YOGA.
Die IHK Saarbrücken bietet auch Lehrgänge mit Zertifikat für Yoga über ihren Kooperationspartner, 
der Werner + Wolf Yoga GmbH & Co KG an.

<br>Unsere Mitglieder möchten gerne wissen, wie der rechtliche Status dieser Lehrgänge ist.
Hierzu haben wir an die IHK Saarbrücken einen Brief geschrieben, auf dessen Antwort wir mit Interesse warten.
<p><a href="ihk-2007-10-24.html">Brief an die IHK Saarbrücken vom 24.10.07
<b>mit</b> Antwort vom 31.10.2007</a>.
<br>Die IHK hat in Ihrer Antwort (oben) auf meine ausdrückliche Frage <b>nicht bestätigt</b>, dass diese Kurs-Zertifikate
 öffentlich-rechtliche Anerkennungen hätten. 
Leider hat sie auch nicht ausdrücklich bestätigt, dass <b>keine</b> öffentlich-rechtliche Anerkennungen vorliegen.
Ich habe desshalb eine nochmal Nachgefragt. 
<br>Die Antwort war diesmal: keine Antwort (bis heute, den 11.2.2008).
<br>Damit kann man feststellen: Die IHK-Yoga Zertifizierungen sind ganz normale 
privatrechtliche Bescheinigungen über den Besuch eines Seminars.
<br>Auch wenn einige das IHK-Zertifikat wie eine Berufsbezeichnung führen - vorläufig ist das so, 
als würde man sich etwa <cit>Liese Müller, medizinische Dokumentation (IHK)</cit>
nennen, weil man den gleichnamigen IHK Zertifikatskurs besucht hat.

<br>Sollte sich die Sachlage in Deutschland ändern (Berufsbild) werden wir die Information an dieser Stelle weiterführen.

<p>In den Medien wurde auch damit geworben, 
dass das IHK Zertifikat "Vorteile" bei der Abrechnung mit Krankenkassen bringen könnte.
Ich habe desshalb die IHK und den Bildungspartner auf den ich verwiesen wurde angeschrieben.
Mit der Frage, ob das IHK-Zertifikat bei irgendeiner Krankenkasse einen Vorteil bringen würde.
<br>Die Antwort war: keine Antwort (bis heute, den 11.2.2008).
Wegen terminlicher Überlastung war es bisher nicht möglich auf mein Schreiben vom 31.10. mit Ja oder Nein zu antworten.
<br>Damit kann man feststellen: Es gibt <b>keine</b> Krankenkasse, die einen Yoga-Lehrer/In mit IHK Zertifikat besser stellt,
als andere Yoga-Lehrer.

