
<h3>IHK Zertifikate und Berufsbild Ayurveda</h3>
<h4>Was bisher geschah....</h4>
Seit Mai 2007 herrschte grosse Unruhe in der Ayurveda-Szene, besonders bei Ausbildungsbetrieben, aber auch bei vielen Einzelanwendern.
<br>
<div style='background-color:lightgrey;font-size:small;width:33%;float:right;'>
Diese Pressemitteilungen wurden mittlerweile längst zurückgezogen.
Wir haben den Text der ersten Pressemitteilung gespeichert und stellen ihn zu dokumentarischen Zwecken zur Verfügung.
Sie können ihn <a target=goo href="http://ubai.info/presseinfo%20im%20portal/presseinfo%20im%20portal.htm">hier</a> nachlesen.
</div>
Eine kleine Firma aus dem Saarland (die Werner + Wolf GmbH & Co KG) hatte es geschafft mit ihrer Werbung und Pressemitteilungen diesen auszulösen.
Denn dort war zu lesen, dass die IHK (Industrie und Handelskammer) nun ab sofort schon ein "Weiterbildungszertifikat" 
namens "Ayurveda-Praktiker (IHK)" anbietet. 
Und das dies schon die Vorstufe zum in Kürze kommenden "staatlich anerkannten" Ayurveda-Beruf wäre.

<p>
<div style='background-color:lightgrey;font-size:small;width:33%;float:right;'>
Der "Workshop" mit Zertifikat namens "Ayurveda-Praktiker (IHK)" 
kostete 3500.- Euro (zuzüglich obligatorischer Unterbringung und Verpflegung zuzüglich Prüfungsgebühr und Reise nach Saarbrücken).
Er wurde nur durch eine Firma durchgeführt, nämlich durch die 
"ROSENBERG Gesellschaft für ganzheitliche Gesundheit und Bildung gemeinnützige GmbH" 
(allgemein als ex-"Mahindra Institut" bekannt, bezeichnet sich regelmäßig selbst als "REAA").
<br>Die kleine Firma ("WW") wurde benötigt, weil nur sie Bildungspartner der IHK Saarland ist und die Rosenberg Gesellschaft, 
weil die "WW" nichts von Ayurveda versteht.
<p>Die IHK Saarland ist eine der (wenigen) Industrie-und-Handelskammern, 
die ihre Seminare durch externe Firmen durchführen lässt.
</div>
Die Unruhe ist verständlich, denn WENN es in Deutschland einen staatlichen Ayurveda-Abschluss gäbe, 
wäre es für ernsthafte Ayurveda-Anbieter langfristig unumgänglich diesen Abschluss vorweisen zu können.
Und dann hätten natürlich auch nur noch Ausbilder eine Chance, die diesen Abschluss vermitteln können.
<br>Wir setzten uns mit der IHK Saarland in Verbindung (diese ist letztendlich der Veranstalter).
Diese bestätigte uns, dass ein Workshop dieses Namens angeboten würde, dass dies aber kein Ausbildungsberuf mit Berufsbild wäre
(schriftlich: "<span style='color:blue;background-color:lightgrey;font:small;'>Korrekt ist, dass dies ein Zertifikatslehrgang ist, der sich am Beispiel
des Fitness-Fachwirtes orientieren wird, so dass eine spätere Überführung,
sofern Weiterbildungsbedarf festgestellt wird, in eine Rechtsverordnung und damit ein Lehrgang
der Aufstiegsfortbildung mit IHK-Prüfung möglich sein kann. 
</span>").

<p><a target=abhyanga href="http://abhyanga.de">Abhyanga.de</a> hatte zu dem Zeitpunkt 599 Einträge, 
davon <a target=abhyanga href="http://ausbildung.abhyanga.de/">über 85 Ayurveda-Ausbildungsbetriebe</a>.
Wir fragten also nach den Inhalten der geplanten Prüfung (nur diese führt die IHK selbst durch).
Um unseren Ausbildungsbetrieben auch die Chance zu geben auf solche Prüfungen vorzubereiten.
Dies wurde uns mit dem Hinweis auf die durch die Kooperationspartner geleistete Vorarbeit "vorerst" verwehrt. 
Man wollte uns Ayurveda-Anwender und -Ausbilder also keine Informationen über unserer zurünftiges Berufsbild geben.
<br>Ein unhaltbarer Zustand.

<h4>Was wir gelernt haben...</h4>
Die Industrie und Handelskammern (IHKn) sind eine öffentlich rechtliche Einrichtung.
Alle Unternehmen, mit Ausnahme reiner Handwerksunternehmen, 
Landwirtschaften und Freiberufler (welche nicht ins Handelsregister eingetragen sind), 
sind zu einer Mitgliedschaft in ihrer regionalen IHK verpflichtet.

<br>Aus bestimmten Strömungen in der Gesellschaft können sich neue Ausbildungsberufe entwickeln.
Wie diese Berufe auszusehen haben und was die Auszubildenden im neuen Beruf lernen sollen wird durch ein Berufsbild definiert.
<br>Für die Erstellung solcher Berufsbilder zuständig sind in Deutschland die Industrie und Handelskammern. 
Das Nähere regelt ein Gesetz 
(<a target=Gesetzestext href="http://www.ihk-wiesbaden.de/fileadmin/user_upload/Geschaeftsfelder/AusundWeiterbildung/Ausbildung/Berufsbildungsreformgesetz.pdf">Gesetzestext hier nachlesen</a>).

<br><span style='background-color:lightgrey;'>Vor der Erstellung eines Berufsbildes <b>müssen</b> dazu natürlich die Stimmen von Berufsverbänden und Standesvertretungen berücksichtigt werden</span>.


<p>Die IHKn bieten auch Ausbildungen und Weiterbildungen an.
Wenn eine IHK ein Seminar durchführt ("Zertifikatslehrgang") stellt sie, wie jeder andere Ausbildungsbetrieb auch, 
ein Abschluss-Zertifikat aus. 
Diese Zertifikate der IHK haben in der Wirtschaft einen guten Namen.
Sie haben aber keinerlei öffentlich-rechtliche Anerkennung, wie z.B. ein akademischer Grad oder ein Abschluss eines Lehrberufs. 
<p>Der bis vor kurzem von der IHK Saarland angebotene einwöchige Workshop ist so ein Zertifikatslehrgang.

<p>Die meissten IHKn führen ihre Seminare nur oder überwiegend mit eigenen Mitarbeitern durch ("wegen schlechter Erfahrungen").
Sie können aber auch externe Firmen mit der Durchführung beauftragen. 
In diesem Fall muss natürlich ein Gleichbehandlungsgrundsatz eingehalten werden, d.h. es darf nicht eine einzelne Firma bei der Beauftragung bevorzugt werden.
Die daraus häufig folgenden Streitigkeiten sind auch der Grund, warum viele IHKn lieber mit eigenen Mitarbeitern Seminare durchführen.


<h4>Folgen des Berufsbildes</h4>
Die Ayurveda-Branche leidet momentan daran, dass es keine einheitlich Ausbildung für Ayurveda-Anwender gibt.
Jeder der will kann sich Ayurveda-Spezialist - Praktiker oder ähnlich nennen.
Das gilt für Kosmetikerinnen, die einmal einen 3-tägigen Massageworkshop besucht haben ebenso wie für Ärzte oder Heilpraktiker, die
sich in jahrelange Ausbildungen begeben haben.

<p>Es gibt also einen Bedarf nach einer Standardisierung, damit die Angebote in etwa vergleichbar sind.
Bislang ist die einzige Möglichkeit für den Kunden danach zu fragen, <b>wo</b> oder <b>in welcher Form</b> die Ausbildung des Anbieters stattgefunden hat
(deshalb gibt es in abhyanga.de schon seit Jahren ein Feld für diese Angabe).

<p>In Österreich gibt es bereits eine staatliche Verordnung, die einen Ayurveda-Ausbildungsstandard definiert ("Ayurveda-Praktiker").
In Indien gibt es ein mehrjähriges Studium dafür.
<p>In Deutschland will dies also jetzt die IHK angehen mit der Erstellung eines Berufsbildes. 
Wir begrüssen das. 
Wie das aber aussehen wird hat weitreichende Folgen.
<ul><li>für Alle, die bislang Ayurveda-Ausbildungen anbieten.
<li>Und für Alle, die bislang Ayurveda-Anwendungen anbieten.
Insbesondere sind erhebliche Kosten für Schulungen und Prüfungen zu erwarten für Inhalte des Berufsbildes, die bislang nicht wichtig waren.
Desshalb ist es sehr wichtig genau abzuwägen, welche Inhalte in ein Berufsbild kommen sollen.
</ul>

<ul><li>Für die tägliche Arbeit und ein qualitätsvolles Arbeiten am Kunden wichtige Dinge <b>müssen</b> enthalten sein
<li>für den Kunden unwichtige Dinge, oder "nice to have"-Themen müssen <b>draussen bleiben</b>, denn sie verteuern unnötig die Ausbildungen und damit die Anwendungen
</ul>


<h4>Was will der UBAI zum Thema IHK und Berufsbild Ayurveda</h4>
<ul>
<li>Wir fordern die IHK auf, unverzüglich die Inhalte des nach ihrer Aussage geplanten Berufsbildes für Ayurveda
zu veröffentlichen und den Ayurveda-Berufsverbänden zur Diskussion zu stellen.

</ul>

