<h5>Erstellung eines Berufsbildes</h5>

Wir sind dabei, zusammen mit einer IHK unsere Vorstellungen für ein Berufsbild für Bereich Ayurveda zu entwickeln.
Uns ist dabei wichtig eine Qualitätsvolle und am Kunden orientierte Ausbildung zu beschreiben, 
die nicht auf das Ausbildungsangebot eines einzelnen Anbieters zugeschnitten ist.


<p>Die IHK Saarland bietet momentan schon einen Workshop an, der nach Auskunft der beteiligten Privatfirmen auch die Erstellung eines Berufsbildes zum Ziel hat.
<br>Die IHK Saarland teilte hierzu schriftlich mit:
<br><span style='color:blue;background-color:lightgrey;'>Korrekt ist, dass dies ein Zertifikatslehrgang ist, der sich am Beispiel
des Fitness-Fachwirtes orientieren wird, so dass eine spätere Überführung,
 sofern Weiterbildungsbedarf festgestellt wird, in eine Rechtsverordnung und damit ein Lehrgang
 der Aufstiegsfortbildung mit IHK-Prüfung möglich sein kann. 
</span>

<p>Die Industrie und Handelskammern sind deutschlandweit durchaus für die Erstellung solcher Berufsbilder zuständig.
