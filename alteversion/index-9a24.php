<h3>IHK Zertifikate und Berufsbild</h3>
<h4>Was bisher geschah....</h4>
Seit Mai 2007 herrschte grosse Unruhe in der Ayurveda-Szene, besonders bei Ausbildungsbetrieben, aber auch bei vielen Einzelanwendern.
<br>Eine kleine Firma aus dem Saarland hatte es geschafft mit ihrer Werbung und Pressemitteilungen diesen auszulösen.
Denn dort war zu lesen, dass die IHK (Industrie und Handelskammer) nun ab sofort schon ein "Weiterbildungszertifikat" 
namens "Ayurveda-Praktiker (IHK)" anbietet und das dies 
<div style='background-color:lightgrey;font-size:small;'>
Diese Pressemitteilungen wurden mittlerweile längst zurückgezogen.
Wir haben den Text der ersten Pressemitteilung gespeichert und stellen ihn zu dokumentarischen Zwecken zur Verfügung.
Sie können ihn <a target=goo href="http://ubai.info/presseinfo%20im%20portal/presseinfo%20im%20portal.htm">hier</a> nachlesen.
</div>

Aus bestimmten Strömungen in der Gesellschaft können sich neue Ausbildungsberufe entwickeln.
Wie diese Berufe auszusehen haben und was die Auszubildenden im neuen Beruf lernen sollen wird durch ein Berufsbild definiert.
<br>Für die Erstellung solcher Berufsbilder zuständig sind in Deutschland die Industrie und Handelskammern. 
Das Nähere regelt ein Gesetz (<a target=Gesetzestext href="http://www.ihk-wiesbaden.de/fileadmin/user_upload/Geschaeftsfelder/AusundWeiterbildung/Ausbildung/Berufsbildungsreformgesetz.pdf">Gesetzestext</a>).

