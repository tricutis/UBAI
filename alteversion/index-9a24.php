<br>Die IHK Saarland teilte hierzu schriftlich mit:
<br><span style='color:blue;background-color:lightgrey;'>Korrekt ist, dass dies ein Zertifikatslehrgang ist, der sich am Beispiel
des Fitness-Fachwirtes orientieren wird, so dass eine spätere Überführung,
sofern Weiterbildungsbedarf festgestellt wird, in eine Rechtsverordnung und damit ein Lehrgang
der Aufstiegsfortbildung mit IHK-Prüfung möglich sein kann. 
</span>
<br>Es ist bislang also erst ein einwöchiger IHK-Lehrgang und noch mit keinerlei Anerkennungen oder Vergünstigungen verbunden.

<h3>Folgen des Berufsbildes</h3>
<p>Sollten es einmal einen eigenen Beruf geben, der sich mit Ayurveda-Anwendungen beschäftigt, 
dann wäre dieser staatlich (öffentlich rechtlich) anerkannt. Das wäre vermutlich gut für diejenigen, die sich mit dieser Anerkennung schmücken können.

<br>Aber es hätte natürlich weitreichende Folgen für Alle, die bislang Ayurveda-Ausbildungen anbieten.
<br>Und für Alle, die bislang Ayurveda-Anwendungen anbieten.
Insbesondere sind erhebliche Kosten für Schulungen und Prüfungen zu erwarten für Inhalte des Berufsbildes, die bislang nicht wichtig waren.
Desshalb ist es sehr wichtig genau abzuwägen, welche Inhalte in ein Berufsbild kommen sollen.
<ul><li>für die tägliche Arbeit und ein qualitätsvolles Arbeiten am Kunden wichtige Dinge müssen enthalten sein
<li>für den Kunden unwichtige Dinge, oder "nice to have"-Themen müssen draussen bleiben, denn sie Verteuern die Ausbildungen und damit die Anwendungen
</ul>

<p>Die Industrie und Handelskammern müssen die Stimmen von Berufsverbänden berücksichtigen.

<br>Bislang sind nur unsere Kollegen vom Berufsverband VEAT beteiligt.
(deren Information zum Projekt Berufsbild finden Sie  <a target=RB href="http://www.ayurveda-akademie.org/00_pdf/workshop_ihk.pdf">hier</a>).
Da der VEAT eng verbunden mit der  Rosenberg .. GmbH (aka REAA) ist, sehen wir die Gefahr, dass deren Vorschläge sich zu sehr am kompletten Ausbildungsprogramm von Rosenberg orientieren. 
<p>
<br>Hier brauchen wir den UBAI als Berufsverband um auch unabhängig von einzelnen Firmen die Interessen einzelner Ausbilder <b>und</b> Anwender 
zu vertreten.

